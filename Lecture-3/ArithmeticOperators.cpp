// ArithmeticOperators.cpp
#include <iostream>
using namespace std;

int main() {

	cout << "5&7  : " <<  (5 & 7) << endl;
	// 5 : 0 1 0 1
	//10 : 1 0 1 0
	cout << "5&10 : " << (5 & 10) << endl;
	cout << "5|7 : " << (5 | 7) << endl;
	cout << "5^7 : " << (5 ^ 7) << endl;
	cout << "5^10 : " << (5 ^ 10) << endl;

	return 0;
}
















